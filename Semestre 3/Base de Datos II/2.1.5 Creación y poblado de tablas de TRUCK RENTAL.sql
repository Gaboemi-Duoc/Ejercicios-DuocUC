CREATE TABLE ARRIENDO_CAMION 
(id_arriendo NUMBER(7) NOT NULL CONSTRAINT PK_ARRIENDO_CAMION PRIMARY KEY,
 nro_patente VARCHAR2(6) NOT NULL,
 numrun_cli NUMBER(10) NOT NULL,
